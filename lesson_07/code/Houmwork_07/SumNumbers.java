package Houmwork_07;
// Напиши программу, которая находит сумму всех целых чисел от 1 до заданного числа n. Число n указывается в коде (хардкод), и программа должна использовать цикл while для выполнения этой задачи.



public class SumNumbers {
    public static void main(String[] args) {
        int n = 10; // Заданное число
        int sum = 0; // Начальная сумма
        int count = 1; // Счётчик, начинаем с 1

        // Цикл while продолжается, пока счётчик <= n
        while (count <= n) {
            sum += count; // Добавляем текущее значение счётчика к сумме
            count++; // Увеличиваем счётчик на 1
        }

        System.out.println("Сумма чисел от 1 до " + n + " равна " + sum);
    }

}

